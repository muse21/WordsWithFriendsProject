using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace WordsWithFriendsProject
{
    //----------------------------------------------------------
    class WordResult_t
    //----------------------------------------------------------
    {
        //----------------------------------------------------------
        // Public Functions
        //----------------------------------------------------------

        //----------------------------------------------------------
        public WordResult_t
            //----------------------------------------------------------
            (
            string aWord,
            Int32 aScore
            )
        {
            mWord = aWord;
            mScore = aScore;
        }

        //----------------------------------------------------------
        // Public Functions
        //----------------------------------------------------------

        public string
        //----------------------------------------------------------
        GetWord
        //----------------------------------------------------------
            (
            )
        {
            return mWord;
        }

        public Int32
        //----------------------------------------------------------
        GetScore
        //----------------------------------------------------------
            (
            )
        {
            return mScore;
        }

        //----------------------------------------------------------
        // Member Data
        //----------------------------------------------------------
        string mWord { get; set; }
        Int32 mScore { get; set; }
    }
}
